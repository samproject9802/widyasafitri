											<h4>7. Sistematika Penulisan Laporan</h4>
											<p align="justify">Judul PKM tidak boleh menggunakan akronim atau singkatan yang tidak baku dan
												hanya diperbolehkan maksimal 20 kata. Mahasiswa menyusun laporan PKM yaitu
												isian kelengkapan (sampul dan pengesahan), halaman utama (daftar isi, halaman inti
												dan lampiran), halaman inti (pendahuluan sampai dengan daftar pustaka) maksimum
												50 halaman. Format penulisan mengikuti sistematika sebagai berikut:</p>
											<p align="justify">SAMPUL</p>
											<p align="justify">LEMBAR PENGESAHAN</p>
											<p align="justify">DAFTAR ISI</p>
											<p align="justify">RINGKASAN</p>
											<p align="justify">BAB 1. PENDAHULUAN</p>
											<p align="justify">Bab ini berisi uraian tentang latar belakang atau justifikasi ilmiah dan
												permasalahan yang akan diteliti. Alasan penelitian tersebut perlu
												diungkapkan melalui pemaparan fenomena nyata yang ditemui peneliti
												jika ada, penelitian yang pernah dilakukan sebelumnya terkait fenomena
												tersebut, serta kesenjangan yang terjadi antara kondisi saat ini dengan
												kondisi yang seharusnya, menurut kajian peneliti. Dituliskan pada bab ini
												tujuan khusus penelitian, manfaat penelitian, keutamaan penelitian,
												temuan yang ditargetkan, kontribusi penelitian terhadap ilmu pengetahuan
												sesuai dengan bidang ilmu pengusul/tim, luaran penelitian.
											<p align="justify">BAB 2. TINJAUAN PUSTAKA</p>
											<p align="justify">Bab ini menguraikan hasil temuan peneliti lain yang diperoleh dari pustaka
												acuan serta menjadi landasan disusunnya proposal. Tinjauan Pustaka
												bukan kumpulan teori, melainkan merupakan rangkaian hasil yang sudah
												dikenali dan mempunyai sebuah atau beberapa alur pikir tentang
												terjadinya suatu peristiwa ilmiah dari suatu topik ilmiah yang akan dikaji
												atau diteliti
											<p align="justify">BAB 3. METODE PENELITIAN</p>
											<p align="justify">Bab ini mengungkapkan metode penelitian yang akan diterapkan, tahapan
												penelitian yang akan dilaksanakan, prosedur penelitian, luaran dan
												indikator capaian yang terukur di setiap tahapan, teknik pengumpulan
												data, analisis data, cara penafsiran, dan penyimpulan hasil penelitian. Bagi
												yang menggunakan metode survei agar melampirkan kuisener lengkap,
												diletakkan setelah lembar daftar pustaka dan tidak dihitung sebagai
												halaman inti.
