				<div align="justify">
					<h4>1. Pendahuluan</h4>
					<p align="justify">Mahasiswa dipandang perlu untuk memahami arti penelitian atau riset, tujuan dan
						manfaatnya dengan maksud membantu mereka memposisikan PKM-P pada jalur
						yang tepat. Pada pedoman ini disertakan arti riset atau penelitian dan beberapa
						klasifikasinya.
						Riset atau penelitian sering dideskripsikan sebagai suatu proses investigasi yang
						dilakukan dengan aktif, tekun, dan sistematis, yang bertujuan untuk menemukan,
						menginterpretasikan, dan merevisi fakta-fakta. Penyelidikan intelektual ini
						menghasilkan suatu pengetahuan yang lebih mendalam mengenai suatu peristiwa,
						tingkah laku, teori, dan hukum, serta membuka peluang bagi penerapan praktis dari
						pengetahuan tersebut.
						Butir-butir berikut dimaksudkan untuk membantu mahasiswa memahami
						beberapa klasifikasi riset sesuai dengan tujuan dan manfaatnya. Dengan demikian
						diharapkan mahasiswa mampu memilih jenis riset yang diminati (Wikipedia
						berbahasa Indonesia).
					</p>
					<p align="justify">1. Penelitian Pengembangan (Development Research) adalah jenis penelitian yang
						bertujuan untuk mengembangkan, memperluas, dan menggali lebih dalam sebuah
						teori yang dimiliki ilmu tertentu. Melalui penelitian-penelitian ini tercipta
						teknologi-teknologi baru yang akhirnya dikenal dengan R dan D (Research and
						Development)</p>
					<p align="justify">2. Penelitian dan Pengembangan, biasa disingkat Litbang (Research and
						Development atau R&D) adalah kegiatan penelitian, dan pengembangan, serta
						memiliki kepentingan komersial dalam kaitannya dengan riset ilmiah murni, dan
						pengembangan aplikatif di bidang teknologi</p>
					<p align="justify">3. Penelitian Eksploratif adalah salah satu jenis penelitian sosial yang bertujuan
						untuk memberikan definisi atau penjelasan mengenai konsep atau pola yang
						digunakan dalam penelitian. Dalam penelitian ini, peneliti belum memiliki
						gambaran akan definisi atau konsep penelitian. Sifat dari penelitian ini adalah
						kreatif, fleksibel, terbuka, dan semua sumber dianggap penting sebagai sumber
						informasi</p>
					<p align="justify">4. Penelitian Verifikatif (Verificative Research) adalah jenis penelitian yang
						bertujuan untuk menguji suatu teori atau hasil penelitian sebelumnya. Dari
						penelitian verifikatifdiperoleh hasil yang memperkuat atau menggugurkan teori
						atau hasil penelitian sebelumnya</p>
					<p align="justify">5. Riset operasi(Operation Research) atau disebut juga riset operasional adalah
						cabanginterdisiplin dari matematika terapan dan sains formal yang menggunakan
						model-model seperti model matematika, statistika, dan algoritma untuk
						mendapatkan nilai optimal atau nyaris optimal pada sebuah masalah yang
						kompleks. Riset operasi biasanya digunakan untuk mencari nilai maksimal (profit,
						performa lini perakitan, hasil panen, bandwith dll.) atau nilai minimal (kerugian,
						risiko, biaya, dll.) dari sebuah fungsi objektif. Riset operasi bertujuan membantu
						manajemen mendapatkan tujuannya melalui proses ilmiah</p>
					<p align="justify">6. Riset pemasaran (Marketing Research) adalah pencarian informasi yang akurat
						dalam bidang pemasaran yang melibatkan konsumen, pelanggan dan public.
						Tujuan riset pemasaran adalah untuk mengidentifikasi dan menetapkan peluang
						dan masalah pemasaran, menciptakan, menjaga dan mengevaluasi kegiatan
						pemasaran. Maksud dari penelitian jenis ini adalah untuk menjadikan topik baru
						lebih dikenal masyarakat luas, memberikan gambaran dasar mengenai topik
						bahasan, menggeneralisasi gagasan dan mengembangkan teori yang bersifat
						tentatif, membuka kemungkinan akan diadakannya penelitian lanjutan terhadap
						topik yang dibahas, serta menentukan teknik dan arah yang akan digunakan dalam
						penelitian berikutnya.</p>
					<p align="justify">7. Riset Pasar (Market Research) adalah sesuatu kegiatan untuk mengetahui apa saja
						yang diperlukan pasar atau masyarakat dan juga mengetahui para pesaing bisnis.
						Dengan diketahuinya apa saja yang dibutuhkan pasar dan juga pesaing yang ada
						dapat membuat produk ataupun jasa yang sesuai dengan pasar dan dapat membuat
						produk atau jasa dapat bersaing di pasaran.</p>
					<div align="justify">
						<h4>2. Tujuan</h4>
						<p align="justify">Tujuan PKM-P adalah menumbuhkembangkan minat dan rasa ingin tahu
							mahasiswa terhadap persoalan, kebutuhan dan tantangan yang dihadapi bangsa
							Indonesia, menemukan solusi ilmiah yang tepat sehingga mampu menghasilkan karya
							penelitian yang bermanfaat baik bagi masyarakat akademik maupun masyarakat luas</p>
						<div align="justify">
							<h4>3. Ruang Lingkup</h4>
							<p align="justify">Disamping menitik beratkan pada unsur kreativitas dan inovasi, PKM-P disarankan
								lebih fokus pada penelitian yang lebih mengutamakan manfaat bagi masyarakat.Secara
								umum PKM-P dapat digolongkan pada PKM-PE (PKM-Penelitian Eksakta) dan PKMPSH (PKM-Penelitian Sosiohumaniora). Kelayakan ketua peneliti dalam melakukan penelitian PKM-PE atau PKMPSH, salah satunya ditentukan oleh kesesuaian topik
								penelitian dengan bidang ilmu yang didalaminya (terlihat dari biodata). Demikian pula
								kelayakan dosen pendamping dalam melakukan pendampingan kegiatan PKM-P,
								ditentukan salah satunya oleh kesesuaian topik penelitian tim PKM-P dengan bidang
								ilmu yang didalami dosen pendamping (terlihat dari biodata), serta aktivitasnya dalam
								membimbing mahasiswa (terlihat dari biodata). Mahasiswa yang berasal dari bidang
								ilmu multidisiplin, seperti perencanaan wilayah, arsitektur, pertanian dalam arti luas,
								kesehatan masyarakat, dan lain-lain, dapat melakukan penelitian pada golongan PKMPE atau PKM-PSH, dengan topik penelitian yang sesuai dengan ilmu multi disiplin
								yang digelutinya.</p>
