							<h4>4. Luaran</h4>
							<p align="justify">Luaran kegiatan PKM adalah poster video yang diunggah ke Youtube dengan isi
								tatakelola futuristic dan konsturktif untuk mencapai tujuan pembangunan
								berkelanjutan atau yang mengandung solusi atas keprihatinan bangsa Indonesia</p>
							<div align="justify">
								<h4>5. Kriteria Pengusulan
									Kriteria, persyaratan dan tata cara pengusulan dijelaskan sebagai berikut :</p>
								</h4>
								<p align="justify">1. Peserta adalah kelompok mahasiswa aktif program Pendidikan S-1 atau Diploma
									yang terdaftar di PD-Dikti</p>
								<p align="justify">2. Kelompok peneliti pengusul berjumlah 3 maxsimal 5 mahasiswa terdiri dari satu orang
									dan anggota peneliti</p>
								<p align="justify">3. Nama semua pengusul (ketua dan anggota) ditulis lengkap dan tidak boleh
									disingkat.</p>
								<p align="justify">4. Bidang kajian harus sesuai dengan bidang ilmu ketua peneliti, atau tim pengusul.
									Anggota dari lintas prodi sangat disarankan.</p>
								<p align="justify">5. Mahasiswa pengusul dapat berasal dari berbagai program studi yang berbeda
									atau dari satu program studi yang sama.</p>
