			<div align="justify">
				<h4>1. Pendahuluan</h4>
				<p align="justify">SDGs 2015-2030 (//www.un.org/sustainabledevelopment/) telah dijadikan tujuan
					pembangunan berkelanjutan di berbagai belahan dunia termasuk Indonesia. Isu yang
					terangkum dalam 17 tujuan pembangunan berkelanjutan tersebut menjadi acuan
					utama dalam pemilihan topik PKM-GFK. Disamping itu berbagai keprihatinan dan
					masalah bangsa Indonesia yang tidak kalah pentingnya juga dapat menjadi acuan.
					PKM-GFK didesain dan diekspos melalui media masa YOUTUBE merupakan salah
					satu cara memanfaatkan era digital yang saat ini melanda dunia. Sementara jenis
					media sosial lainnya seperti instagram, facebook dan lain-lainnya, untuk sementara
					waktu belum dimanfaatkan sebagai wahana untuk mengekspos PKM-GFK.</p>
				<div align="justify">
					<h4>2. Tujuan</h4>
					<p align="justify">PKM-GFK bertujuan untuk memotivasi partisipasi mahasiswa dalam mengelola
						imajinasi, persepsi dan nalarnya, memikirkan tatakelola yang futuristik namun
						konstruktif sebagai upaya pencapaian tujuan SDGs di Indonesia ataupun solusi
						keprihatinan bangsa Indonesia. Kesemuanya dimulai dari gagasan, lalu dilanjutkan
						narasi, yang pada akhirnya diwujudkan dalam karya.</p>
					<div align="justify">
						<h4>3. Ruang Lingkup</h4>
						<p align="justify">PKM-GFK mengacu kepada 17 tujuan pembangunan berkelanjutan, SDGs berikut :</p>
						<p align="justify">a. Tanpa kemiskinan (no poverty) yaitu tidak ada kemiskinan dalam bentuk apapun di seluruh penjuru dunia</p>
						<p align="justify">b. Tanpa kelaparan (zero hunger) yaitu tidak ada lagi kelaparan, mencapai ketahanan pangan, perbaikan nutrisi serta mendorong budidaya pertanian yang berkelanjutan</p>
						<p align="justify">c. Kesehatan yang baik dan kesejahteraan (good health and well-being) yaitu menjamin kehidupan yang sehat serta mendorong kesejahteraan bagi seluruh masyarakat di segala umur</p>
						<p align="justify">d. Pendidikan berkualitas (quality education) yaitu menjamin pemerataan pendidikan yang berkualitas dan meningkatkan kesempatan belajar untuk semua orang</p>
						<p align="justify">e. Kesetaraan gender (gender equality) yaitu mencapai kesetaraan gender dan memberdayakan kaum ibu dan perempuan</p>
						<p align="justify">f. Air bersih dan sanitasi (clean water and sanitation) yaitu menjamin ketersediaan air bersih dan sanitasi yang berkelanjutan untuk semua orang</p>
						<p align="justify">g. Energi bersih dan terjangkau (affordable and clean energy) yaitu menjamin akses terhadap sumber energi yang terjangkau, terpercaya, berkelanjutan dan modern untuk semua</p>
						<p align="justify">h. Pertumbuhan ekonomi dan pekerjaan yang layak (decent work and economic growth) yaitu mendukung perkembangan ekonomi yang berkelanjutan, lapangan kerja yang produktif serta pekerjaan yang layak untuk semua orang]</p>
						<p align="justify">i. Industri, inovasi dan infrastruktur (industry, innovation and infrastructure) yaitu membangun infrastruktur yang berkualitas, mendorong peningkatan industri yang berkelanjutan serta mendorong inovasi</p>
						<p align="justify">j. Mengurangi kesenjangan (reduced inequalities) yaitu mengurangi ketidaksetaraan baik di dalam sebuah negara maupun di antara negara-negara di dunia</p>
						<p align="justify">k. Keberlanjutan kota dan komunitas (sustainable cities and cummunities) yaitu membangun kota-kota serta pemukiman yang berkualitas, aman dan berkelanjutan</p>
						<p align="justify">l. Konsumsi dan produksi bertanggungjawab (responsible consumption and production) yaitu menjamin keberlangsungan konsumsi dan pola produksi</p>
						<p align="justify">m. Aksi terhadap iklim (climate action) yaitu bertindak cepat untuk memerangi perubahan iklim dan dampaknya</p>
						<p align="justify">n. Kehidupan bawah laut (live below water) yaitu melestarikan dan menjaga kesinambungan laut dan kehidupan sumber daya laut untuk perkembangan yang berkelanjutan</p>
						<p align="justify">o. Kehidupan di darat (life on land) yaitu melindungi, mengembalikan danmeningkatkan keberlangsungan pemakaian ekosistem darat, mengelola hutan secara berkelanjutan, mengurangi tanah tandus serta tukar guling tanah</p>
						<p align="justify">p. Institusi peradilan yang kuat dan kedamaian (peace, justice and strong institution) yaitu meningkatkan perdamaian termasuk masyarakat untuk pembangunan berkelanjutan, menyediakan akses untuk keadilan bagi semua orang termasuk lembaga dan bertanggungjawab untuk seluruh kalangan</p>
						<p align="justify">q. Kemitraan untuk mencapai tujuan (partnership for the goal) yaitu memperkuat implementasi dan menghidupkan kembali kemitraan global untuk pembangunan yang berkelanjutan</p>
						<p align="justify">Isu nasional yang menjadi keprihatinan bangsa Indonesia juga dapat menjadi acuan dalam penyusunan PKM-GFK antara lain: Korupsi, Narkoba, NKRI, Bencana alam, Bahasa daerah dan Konservasi satwa liar</p>
