									<h4>6. Sistematika Penulisan Laporan</h4>
									<p align="justify">Judul PKM tidak boleh menggunakan akronim atau singkatan yang tidak baku dan
										hanya diperbolehkan maksimal 20 kata. Mahasiswa menyusun laporan PKM yaitu
										isian kelengkapan (sampul dan pengesahan), halaman utama (daftar isi, halaman inti
										dan lampiran), halaman inti (pendahuluan sampai dengan daftar pustaka) maksimum
										10 halaman. Format penulisan mengikuti sistematika sebagai berikut:</p>
									<p align="justify">SAMPUL</p>
									<p align="justify">DAFTAR ISI</p>
									<p align="justify">SINOPSIS KONTEN MAKSIMAL 500 KATA</p>
									<p align="justify">BAB 1 PENDAHULUAN (Sumber Inspirasi Tantangan Intelektual)</p>
									<p align="justify">Pendahuluan mencakup latar belakang atas isu atau permasalahan serta
										urgensi dan rasionalisasi kegiatan. Tujuan kegiatan dan gagasan
										pemecahan masalah disajikan dalam bagian ini. Tinjauan pustaka yang
										relevan merujuk berbagai sumber pustaka, pandangan singkat para
										pelaksana/praktisi lain yang pernah melakukan pembahasan topik terkait
										untuk menerangkan kemutakhiran dan kreativitas substansi karya serta
										manfaat dan keberlanjutannya, dimasukkan dalam bagian ini.
										Pendahuluan merupakan uraian, deskripsi, rangkuman, yang menjelaskan
										isi video secara umum, kutipan bagian yang memperjelas isi video,
										kaitannya dengan konteks situasi yang sedang hangat di masyarakat atau
										SDGs, yang bersifat pemancing untuk menarik perhatian audiens.
										Pendahuluan dapat dimulai dengan memperkenalkan pelaksana, karya
										yang pernah dihasilkan berbentuk apa saja, dan prestasi apa yang pernah
										diperoleh. Selanjutnya dapat juga membandingkan dengan video sejenis
										yang sudah diproduksi, baik oleh pelaksana sendiri maupun pembuat yang
										lain. Pendahuluan boleh saja memaparkan kekhasan atau sosok pelaksana,
										memaparkan keunikan karya video, merumuskan tema video,
										mengungkapkan kritik terhadap kelemahan video yang mungkin masih
										ada (otokritik) serta mengungkapkan kesan terhadap video</p>
									<p align="justify">BAB 2 METODE PELAKSANAAN</p>
									<p align="justify">Secara umum, metode berisi tentang bagaimana pelaksanaan dilakukan
										termasuk waktu, lama, dan tempat. Di samping itu juga menjelaskan bahan
										dan alat yang digunakan, teknik untuk berkarya, cara olah produksi dan
										evaluasi proses berkarya yang dilakukan. Maka selanjutnya tiga aspek
										penting dalam metode pembuatan film pada akhirnya dapat ditekankan,
										yakni mengolah Fiksi, menampilkan aspek Dokumentatif dan upaya
										eksperimental.
									<p align="justify">BAB 3 HASIL</p>
									<p align="justify">Laporan PKM-GFK menggambarkan fungsi informatif, yakni
										menginformasikan keberadaan isu terkait karya video, sehingga audiens
										merasa tertarik untuk mengetahuinya lebih lanjut. Tulisan diharapkan
										memberikan fungsi akademik, yakni presensi dalam membentuk wacana
										keilmuan serta berbagai pengalaman dan sudut pandang tentang topik
										tertentu yang dijadikan fokus ulasan. Tujuan Laporan ini adalah
										memberikan sebuah pemahaman & informasi secara komprehensif kepada
										suatu masyarakat atau audiens tentang isi video yang diulasnya. Mengajak
										audiens agar mendiskusikan dan memikirkan lebih jauh tentang
										permasalahan yang diangkat yang ada di dalam video tersebut.
										Memberikan suatu pertimbangan kepada audiens tentang
										baik/bagus/pantas atau tidaknya video itu untuk disaksikan atau
										dipublikasikan. Memberikan suatu jawaban mengenai pertanyaanpertanyaan yang mungkin timbul dari audiens ketika video dirilis.
										Memberikan sugesti kepada audiens, apakah sebuah video/film patut
										disaksikan/ditonton secara luas. Melukiskan dan memaparkan
										pendapatnya melalui sebuah pertimbangan atau penilaian. Memberikan
										kriteria-kriteria yang jelas dalam mengemukakan pendapatnya itu. Dalam
										kaitannya dengan tujuan tersebut maka diperlukan pertimbangan untuk
										menyajikan hal-hal menyangkut keinginan pelaksana yang disajikan
										dalam resensi secara metaanalisis, yaitu mengkaji landasan filosofis dan
										akademis yang dijadikan dasar karya video. Kajian yang dimaksud
										misalnya seperti landasan idealisme, paradigma audiens yang hendak
										dikembangkan melalui karyanya, dan fokus pengembangan ketrampilan
										dan pengenalan bagi audiens terhadap karya. Dapat juga digambarkan
										kepentingan audiens sehingga bisa terbantu mendapatkan informasi yang
										diperlukan sehingga antara pelaksana dan audiens bisa terjembatani
										dengan hadirnya tulisan ulasan ini. Materi video disajikan untuk
										menunjukkan bahwa materi tersebut tepat bagi segmen audiens yang
										menjadi targetnya.
