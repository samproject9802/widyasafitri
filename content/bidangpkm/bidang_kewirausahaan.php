			<div align="justify">
				<h4>1. Pendahuluan</h4>
				<p align="justify">PKM-K tidak semata-mata berorientasi pada laba (profit), tetapi lebih
					mengutamakan jenis komoditas usaha yang menunjukkan kepakaran tim. Komoditas
					usaha yang dihasilkan mahasiswa dapat berupa barang atau jasa yang selanjutnya
					merupakan salah satu modal dasar mahasiswa dalam berwirausaha dan memasuki
					pasar. Komoditas tim PKM-K hendaknya tidak menjadi kompetitor produk sejenis
					yang merupakan penghasilan masyarakat. Pelaku utama berwirausaha dalam hal ini
					adalah mahasiswa, bukan masyarakat, ataupun mitra lainnya</p>
				<div align="justify">
					<h4>2. Tujuan</h4>
					<p align="justify">Tujuan PKM-K adalah memotivasi dan membuka peluang bagi mahasiswa untuk
						menghasilkan karya kreatif, inovatif sebagai bekal berwirausaha sebelum atau setelah
						menyelesaikan studi. PKM-K diharapkan dapat menjadi cikal bakal kemunculan
						produk usaha di Indonesia sebagai karya mandiri bangsa.</p>
					<div align="justify">
						<h4>3. Ruang Lingkup</h4>
						<p align="justify">Meskipun kuliner paling memenuhi kriteria yang berorientasi profit, namun
							PKM-K sejatinya lebih mengutamakan solusi tantangan intelektual yang mendasari
							lahirnya komoditas usaha baru dan unik sebagai komoditas usaha harus merupakan
							jelmaan penguasaan iptek mahasiswa.</p>
						<div align="justify">
							<h4>4. Luaran</h4>
							<p align="justify">Luaran wajib kegiatan adalah poster dan video yang diunggah ke YOUTUBE
								dari kegiatan dari hasil produk usaha (barang atau jasa komersial).</p>
							<div align="justify">
								<h4>5. Kriteria Pengusulan
									Kriteria, persyaratan dan tata cara pengusulan dijelaskan sebagai berikut :</p>
								</h4>
