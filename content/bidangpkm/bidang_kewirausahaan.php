									yang terdaftar di PD-Dikti</p>
								<p align="justify">2. Kelompok peneliti pengusul berjumlah 3 maxsimal 5 mahasiswa terdiri dari satu orang
									dan anggota peneliti</p>
								<p align="justify">3. Nama semua pengusul (ketua dan anggota) ditulis lengkap dan tidak boleh
									disingkat.</p>
								<p align="justify">4. Bidang kajian harus sesuai dengan bidang ilmu ketua peneliti, atau tim pengusul.
