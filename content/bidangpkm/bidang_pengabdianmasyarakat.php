					<h1>PROGRAM KREATIVITAS MAHASISWA</h1>
					<h2>Pengabdian Kepada Masyarakat</h2>
					<h4>(PKM-M)</h4>
				</div>
				<div align="justify">
					<h4>1. Pendahuluan</h4>
					<p align="justify">PKM-M merupakan program bantuan ilmu pengetahuan, teknologi dan seni
						dalam upaya menyelesaikan persoalan atau memenuhi kebutuhan kehidupan
						masyarakat yang tidak berorientasi pada profit. Dalam hal ini mitra antara lain
						sekolah, panti asuhan, pemerintah, karang taruna, kelompok PKK atau masyarakat
						lainnya. PKM-M dapat berupa upaya untuk membangun keterampilan usaha,
						penataan dan perbaikan lingkungan, penguatan kelembagaan masyarakat, pengenalan
						dan pemahaman aspek hukum adat, upaya mengatasi buta aksara danlain-lain.
						Dalam penyusunan proposal, mahasiswa tidak harus turun langsung ke
						masyarakat mitra, mengobservasi keadaan nyata terkini masyarakat mitra dan
						bertukar pikiran dengan masyarakat karena guna mematuhi protokol kesehatan.
						Untuk dapat mengidentifikasi permasalahan dapat dilakukan dengan pengamatan
						masalah dengan data sekunder yang didapatkan dari internet ataupun masalah yang
						dialami dari tim kelompok.</p>
					<div align="justify">
						<h4>2. Tujuan</h4>
						<p align="justify">Tujuan PKM-M adalah memotivasi mahasiswa untuk menumbuhkan empatinya terhadap persoalan dan kebutuhan masyarakat. Sekaligus untuk memberi kemanfaatan eksistensinya bagi masyarakat.</p>
						<div align="justify">
							<h4>3. Ruang Lingkup</h4>
							<p align="justify">Ruang lingkup meliputi (1) kewirausahaan masyarakat non produktif untuk pembentukan wirausaha baru, (2) penerapan ilmu pengetahuan, teknologi dan seni untuk memecahkan persoalan atau memenuh kebutuhan masyarakat, dan (3)
								pemberdayaan masyarakat untuk meningkatkan kualitas kehidupan masyarakat atau lingkungan.</p>
							<div align="justify">
								<h4>4. Luaran</h4>
								<p align="justify">Luaran wajib kegiatan adalah poster dan video yang diunggah ke YOUTUBE dari kegiatan dari hasil PKM.</p>
								<div align="justify">
									<h4>5. Kriteria Pengusulan</h4>
									<p align="justify">Kriteria, persyaratan dan tata cara pengusulan dijelaskan sebagai berikut :</p>
									<p align="justify">1. Peserta adalah kelompok mahasiswa aktif program Pendidikan S-1 atau Diploma yang terdaftar di PD-Dikti</p>
									<p align="justify">2. Kelompok peneliti pengusul berjumlah sesuai table 1.1, terdiri dari satu orang dan anggota peneliti</p>
									<p align="justify">3. Nama semua pengusul (ketua dan anggota) ditulis lengkap dan tidak boleh disingkat.</p>
									<p align="justify">4. Bidang kajian harus sesuai dengan bidang ilmu ketua peneliti, atau tim pengusul. Anggota dari lintas prodi sangat disarankan</p>
									<p align="justify">5. Mahasiswa pengusul dapat berasal dari berbagai program studi yang berbeda atau dari satu program studi yang sama.</p>
									<p align="justify">6. Besaran Rencana Anggaran Biaya pada dana Penelitian sebesar Rp.5.000.000 s/d Rp.12.500.000.</p>
