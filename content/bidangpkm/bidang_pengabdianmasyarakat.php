											<h4>7. Sistematika Penulisan Laporan</h4>
											<p align="justify">Judul PKM tidak boleh menggunakan akronim atau singkatan yang tidak baku dan hanya diperbolehkan maksimal 20 kata. Mahasiswa menyusun laporan PKM yaitu isian kelengkapan (sampul dan pengesahan), halaman utama (daftar isi, halaman inti
												dan lampiran), halaman inti (pendahuluan sampai dengan daftar pustaka) maksimum 50 halaman. Format penulisan mengikuti sistematika sebagai berikut:</p>
											<p align="justify">SAMPUL</p>
											<p align="justify">LEMBAR PENGESAHAN</p>
											<p align="justify">DAFTAR ISI</p>
											<p align="justify">RINGKASAN</p>
											<p align="justify">BAB 1. PENDAHULUAN</p>
											<p align="justify">Uraikan latar belakang disusunnya proposal dengan mengungkap identitas mitra dan permasalahan yang dihadapi mereka dan seberapa tinggi level urgensinya dibandingkan persoalannyata terkini lainnya. Dalam menentukan solusi masalah mitra, diharapkan mahasiswa mengkaji secara kritis dan mendalam akar-akar permasalahan, berpikir secara kreatif untuk memilih berbagai alternatif solusi untuk memecahkan masalah serta bersama-sama mitra menentukan solusi yang diyakini paling efektif dalam memecahkan masalah tersebut Target luaran yang diharapkan dari kegiatan ini dan manfaat kegiatan harus disajikan pada bab ini.
											<p align="justify">BAB 2. TINJAUAN PUSTAKA</p>
											<p align="justify">Pada bab ini diuraikan secara kuantitatif jumlah, profil dan kondisi sosial
												ekonomi masyarakat sasaran yang akan bekerjasama dilibatkan dalam
												kegiatan. Uraikan pula kondisi dan potensi wilayah dari aspek fisik, sosial,
												ekonomi maupun lingkungan yang relevan dengan kegiatan yang akan
												dilakukan.
											<p align="justify">BAB 3. METODE PENELITIAN</p>
											<p align="justify">Pada bab ini uraikan secara jelas pola dan frekuensi pelaksanaan program
												(pelatihan, pendampingan, demplot, konsultasi, rekayasa sosial dll),
												teknik pengumpulan, pengolahan dan analisis data serta tahapan pekerjaan
												dalam pencapaian tujuan program. Hindari adanya kegiatan penelitian
												atau kegiatan survei dalam proposal.
											<p align="justify">BAB 4. BIAYA DAN JADWAL KEGIATAN</p>
											<p align="justify">4.1 Anggaran Biaya</p>
											<p align="justify">Rekomendasi untuk pengalokasian dan penggunaan dana 80% untuk
												kebutuhan operasional dan 20% untuk kebutuhan administrasi. Biaya
												perjalanan dilakukan seefisien dan seminimal mungkin. Biaya perjalanan
												yang berbasis aktivitas lapangan, disusun seefisien dan seoptimal mungkin
												mengacu kepada peraturan Menteri Keuangan RI. Dengan memperhatikan
												proses pengelolaan PKM berbasis daring, item biaya yang tidak
												diperkenankan diusulkan dalam RAB adalah:</p>
											<p align="justify">Honorarium untuk Tim, Dosen Pendamping atau Pihak ke 3</p>
											<p align="justify">Konsumsi untuk Tim, Dosen Pendamping atau Pihak ke 3</p>
											<p align="justify">Pembelian atau penyewaan perangkat berupa Komputer PC, Laptop,
												Printer, Ponsel, Kamera, Handycam, sewa laboratorium, peralatan
												laboratorium lainnya (jika sifatnya wajib agar besarannya tidak
												melebihi Rp 1.500.000,-)</p>
											<p align="justify">Penyusunan, penggandaan dan penjilidan laporan kemajuan, laporan
												akhir</p>
											<p align="justify">Kertas tidak lebih dari 2 rim, ATK sesuai kebutuhan</p>
											<p align="justify">Perjalanan pengambilan data</p>
											<p align="justify">Rekapitulasi rencana anggaran biaya disusun sesuai dengan kebutuhan dan
												disusun mengikuti format pada tabel RAB.</p>
											<p align="justify">4.2. Jadwal Kegiatan</p>
											<p align="justify">Jadwal kegiatan antara 3 s.d 5 bulan, disusun dalam bentuk bar chart sesuai
												dengan format pada lampiran.</p>
											<p align="justify">BAB 5 HASIL DAN PEMBAHASAN</p>
											<p align="justify">Hasil dan pembahasan dilakukan dengan membentuk sebuah prototipe
												atau simulasi sebuah sistem yang diimpelementasikan. Jika PKM-M
												melakukan kegiatan pelatihan ataupun sejenisnya maka pembahasan
												dilakukan dari modul materi yang digunakan selama Latihan dan tata cara
												pelaksanaan kegiatan pelatihan tersebut termasuk evaluasi dari hasil
												kegiatan pengabdian masyarakat yang dilakukan.</p>
