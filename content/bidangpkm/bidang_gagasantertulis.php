											<h4>8. Seleksi dan Penilaian Laporan</h4>
											<p align="justify">Seleksi laporan PKM untuk dapat diusulkan ke dirjen belmawa memiliki komponen
												penilaian seperti berikut :</p>
											<p align="justify">a. Kreatif dan Objektif : tulisan berisi gagasan kreatif yang menawarkan solusi suatu
												permasalahan, kebutuhan atau tantangan yang dihadapi masyarakat. Tulisan tidak
												bersifat emosional atau subjektif, didukung data dan/atau informasi terpercaya,
												bersifat asli (bukankarya jiplakan).</p>
											<p align="justify">b. Logis dan Sistematis: tiap langkah penulisan dirancang secara sistematis, runut,
												memuat unsur-unsur identifikasi masalah, analisis sintesis, kesimpulan serta
												memuat saran-saran.</p>
											<p align="justify">c. Isi tulisan berdasarkan telaah pustaka atau sumber informasi lainnya</p>
											<p align="justify">d. Materi Karya Tulis : materi yang ditulis tidak harus sejalan dengan bidang ilmu
												yang sedang ditekuni para penulis/mahasiswa. Kesempatan ini diberikan kepada
												mahasiswa yang memiliki ide kreatif dan mampu menuangkannya dalam bentuk
												tulisan, walaupun yang bersangkutan tidak sedang belajar secara formal di bidang
												tersebut. Materi karya tulis merupakan isu mutakhir atau actual</p>
