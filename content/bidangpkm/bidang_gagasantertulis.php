										<h4>7. Sistematika Penulisan Laporan</h4>
										<p align="justify">Judul PKM tidak boleh menggunakan akronim atau singkatan yang tidak baku dan
											hanya diperbolehkan maksimal 20 kata. Mahasiswa menyusun laporan PKM yaitu
											isian kelengkapan (sampul dan pengesahan), halaman utama (daftar isi, halaman inti
											dan lampiran), halaman inti (pendahuluan sampai dengan daftar pustaka) maksimum
											50 halaman. Format penulisan mengikuti sistematika sebagai berikut:</p>
										<p align="justify">SAMPUL</p>
										<p align="justify">LEMBAR PENGESAHAN</p>
										<p align="justify">DAFTAR ISI</p>
										<p align="justify">RINGKASAN</p>
										<p align="justify">BAB 1. PENDAHULUAN</p>
										<p align="justify">Bagian Pendahuluan menguraikan latar belakang yang mengungkap
											tentang situasi dan kondisi bangsa, negara yang menjadi alasan
											mengangkat gagasan menjadi PKM-GT (dilengkapi dengan data atau
											informasi yang mendukung). Bagian ini juga mengungkap tujuan dan
											besarnya manfaat yang ingin dicapai.
										<p align="justify">BAB 2. GAGASAN</p>
										<p align="justify">Bagian gagasan berisi uraian tentang:</p>
										<p align="justify">a. Kondisi terkini pencetus gagasan (diperoleh dari bahan bacaan,
											wawancara/diskusi,observasi, imajinasi yang relevan)</p>
										<p align="justify">b. Solusi yang pernah diterapkan untuk memperbaiki kondisi pencetus gagasan</p>
										<p align="justify">c. Seberapa jauh kondisi pencetus gagasan dapat diperbaharui atau dikembangkan jika gagasan tersebut diimplementasikan </p>
										<p align="justify">d. Pihak-pihak yang dipertimbangkan dapat membantu mengimplementasikan gagasandan peran atau kontribusi masingmasingnya</p>
										<p align="justify">e. Langkah-langkah strategis yang harus dilakukan untuk mengimplementasikan gagasan sehingga tujuan atau pembaruan yang diharapkan dapat tercapai.</p>
