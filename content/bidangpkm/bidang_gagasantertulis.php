			<div align="justify">
				<h4>1. Pendahuluan</h4>
				<p align="justify">Sebagai bangsa yang besar dan mendiami daratan, lautan luas dapat dipastikan
					banyaknya persoalan, kebutuhan dan tantangan yang dihadapi Indonesia. Ada yang
					dapat diatasi dalam jangka waktu pendek akan tetapi banyak di antara persoalan,
					tantangan dan kebutuhan baru akan terselesaikan dikemudian hari. Sebagai
					mahasiswa yang merupakan bagian bangsa ini tentu wajib membantu menemukan
					solusi yang tepat. Apakah dalam wujud aksi berjangka waktu pendek atau pemikiran
					realistis yang memerlukan waktu lama. PKM-GT membuka peluang bagi mahasiswa
					menuangkan ide realistik kreatif sebagai respons intelektual atas persoalan aktual
					yang dihadapi bangsa. Ide tersebut tidak terikat bidang ilmu, bersifat unik, realistik
					kreatif, visioner/futuristik dan bermanfaat sehingga kampus yang diidealisasikan
					sebagai pusat solusi dapat menjadi kenyataan. Sebagai intelektual muda, mahasiswa
					umumnya cenderung pandai mengungkapkan fakta sosial, dan melalui PKM-GT,
					level nalar mahasiswa tidak hanya dituntut sampai sebatas mengekspos fakta tetapi
					justru harus mampu memberi atau menawarkan solusi. Ide realistik kreatif dalam
					PKM-GT, jika diimplementasikan memerlukan waktu yang lama, biaya dan sumber
					daya yang besar. Jadi PKM-GT tidak seperti PKM 5 Bidang yang dapat diselesaikan
					dalam waktu 3-5 bulan. PKM-GT adalah konsep perubahan atau pengembangan yang
					bersifat futuristik, realistik dan jangka panjang. Ide realistik dari PKM-GT ini tidak
					dapat diselesaikan dalam waktu pendek melalui PKM 5 bidang. Sebagai salah satu
					PKM yang ditampilkan dalam PIMNAS, maka tata tertib dan segala sesuatu yang
					terkait pada persyaratan presentasi mengacu pada tatacara pelaksanaan PIMNAS.</p>
				<div align="justify">
