			<div align="justify">
				<h4>1. Pendahuluan</h4>
				<p align="justify">Berbeda dengan kelima jenis PKM sebelumnya yang melibatkan pelaksanaan
					kegiatan fisik di laboratorium ataupun lapangan, PKM-AI tidak mengenal adanya
					kegiatan semacam itu. Jika dalam kelima jenis PKM sebelumnya, kelompok
					mahasiswa mengajukan proposal kegiatan ke Ditjen Belmawa, maka untuk PKM-AI
					kelompok mahasiswa cukup menyampaikan karya tulis dalam bentuk artikel ilmiah
					dikirimkan secara daring. Karya tersebut ditulis mengacu pada kegiatan yang telah
					selesai dilakukan kelompok mahasiswa, tetapi bukan berasal dari PKM dan juga
					bukan kegiatan insentif pembiayaan lain yang mewajibkan luaran artikel ilmiah.
					Kegiatan berkelompok yang dapat ditulis menjadi artikel ilmiah untuk proposal PKMAI diantaranya adalah hasil Praktek Kerja Lapangan (PKL) atau Praktek Pengalaman
					Lapangan (PPL), kuliah kerja nyata (KKN) atau kegiatan akademik berkelompok
					lainnya, tetapi bukan tugas-tugas atau praktikum individu perkuliahan, skripsi atau
					tugas akhir. Kelompok penulis yang artikel ilmiahnya dinilai baik dan layak
					dipublikasik n, akan memperoleh insentif dana tunai sebesar Rp 3.000.000 (tiga juta
					rupiah). AI akan dipublikasikan di e-journal Ditjen Belmawa.
					PKM-AI diharapkan dapat meningkatkan kemampuan mahasiswa dalam
					menuangkan pemikiran dan hasil-hasil kegiatan ilmiah yang telah dilakukan ke dalam
					bentuk sebuah artikel ilmiah sesuai kriteria standar penulisan jurnal ilmiah. Dengan
					demikian, program ini diharapkan mampu mengantarkan mahasiswa kepada
					keterampilan atau kemahiran menulis. Melalui kemahiran tersebut, mahasiswa secara
					runut mampu menguraikan suatu permasalahan sehingga mendorong perlunya usaha
					penyelesaian atau pencarian solusi dengan tujuan tertentu, baik untuk permasalahan
					yang masih dasar maupun terapan di lapangan. Disamping itu, mahasiswa juga
					mampu memilih teknik dan landasan metode penyelesaian masalah disertai dengan
					kemampuan menguraikan landasan teori yang terkait dengan permasalahan yang
					dibahas, serta ketajaman pembahasan dalam menganalisis hasil yang diperoleh, yang
					akhirnya bermuara pada penyimpulan upa a penyelesaian masalah yang telah
					dilakukan. Ada tiga karakter utama PKM-AI yaitu :</p>
				<p align="justify">a. Tidak ada usulan pembiayaan</p>
				<p align="justify">b. Usulan berupa artikel ilmiah siap terbit yang mengikuti kelaziman kaidah
					penulisan suatu jurnal ilmiah</p>
				<p align="justify">c. Sumber penulisan artikel ilmiah tersebut adalah kegiatan yang telah selesai
					dilakukan kelompok mahasiswa penulis artikel. Karakter terakhir ini sekaligus
					menunjukkan bahwa sumber penulisan merupakan kegiatan, bukan laporan.</p>
				<p align="justify">Dalam PKM, kreativitas dan kerja sama tim merupakan dua unsur yang
					diprioritaskan. Oleh karena itu, sejak dimulainya implementasi PKM-I tahun 2006
					dan PKM-AI 2009, penulisan PKM-AI dalam rangka Skripsi atau Tugas Akhir yang
					bersifat individu tidak diperkenankan karena tidak ada unsur kerjasama tim.
					Demikian pula hasil praktikum tidak diperkenankan dijadikan sumber artikel PKMAI karena tidak ada unsur kreativitas.</p>
				<div align="justify">
					<h4>2. Tujuan</h4>
					<p align="justify">Tujuan dari kegiatan PKM-AI adalah menumbuh kembangkan minat dan
						kemampuan menulis ilmiah mahasiswa.</p>
					<div align="justify">
						<h4>3. Ruang Lingkup</h4>
						<p align="justify">PKM-AI meliputi kegiatan Tri Dharma Perguruan Tinggi, yaitu Pendidikan,
							Penelitian dan Pengabdian Kepada Masyarakat. Dalam hal ini yang dijadikan lingkup
							adalah hasil kegiatan tim dan bukan individu.</p>
