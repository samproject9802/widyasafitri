							<div align="justify">
								<h4>5. Kriteria Pengusulan
									Kriteria, persyaratan dan tata cara pengusulan dijelaskan sebagai berikut :</p>
								</h4>
								<p align="justify">1. Peserta adalah kelompok mahasiswa aktif program Pendidikan S-1 atau Diploma
									yang terdaftar di PD-Dikti</p>
								<p align="justify">2. Kelompok peneliti pengusul berjumlah 3 maxsimal 5 mahasiswa terdiri dari satu orang
									dan anggota peneliti</p>
								<p align="justify">3. Nama semua pengusul (ketua dan anggota) ditulis lengkap dan tidak boleh disingkat.</p>
								<p align="justify">4. Bidang kajian harus sesuai dengan bidang ilmu ketua peneliti, atau tim pengusul. Anggota dari lintas prodi sangat disarankan.</p>
								<p align="justify">5. Mahasiswa pengusul dapat berasal dari berbagai program studi yang berbeda atau dari satu program studi yang sama.</p>
								<div align="justify">
									<h4>6. Sistematika Penulisan Laporan</h4>
									<p align="justify">Judul PKM tidak boleh menggunakan akronim atau singkatan yang tidak baku dan
										hanya diperbolehkan maksimal 20 kata. Mahasiswa menyusun laporan PKM yaitu
										isian kelengkapan (sampul dan pengesahan), halaman utama (daftar isi, halaman inti
										dan lampiran), halaman inti (pendahuluan sampai dengan daftar pustaka) maksimum
										10 halaman. Format penulisan mengikuti sistematika sebagai berikut::</p>
									<p align="justify">JUDUL</p>
									<p align="justify">Judul tulisan hendaknya menggambarkan isi pokok tulisan secara ringkas dan jelas. Judulmaksimum 20 kata, hindari penggunaan akronim yang tidak umum/baku.
									<p align="justify">NAMA PENULIS</p>
									<p align="justify">Nama-nama penulis dituliskan tepat di bawah judul, disertai dengan
										alamat institusi penulis, serta catatan kaki untuk penulis korespondensi.
									<p align="justify">ABSTRAK DAN ABSTRACT (Maksium satu halaman)</p>
									<p align="justify">Abstrak ditulis dalam Bahasa Indonesia dan Inggris. Abstrak berisi tidak lebih dari 250 kata dan merupakan intisari seluruh tulisan yang meliputi: latar belakang, tujuan, metode, hasil dan kesimpulan dan ditulis dengan
										jarak baris 1,0 spasi. Di bawah abstrak disertakan 3-5 kata-kata kunci(keywords).
									<p align="justify">PENDAHULUAN</p>
									<p align="justify">Pendahuluan merupakan gambaran umum dari observasi awal dan
										fenomena mengenai topik yang diangkat. Latar belakang, rumusan, tujuan
										dari kegiatan (penelitian, pengabdian, atau yang lainnya) serta manfaat
										untuk waktu yang akan datang ditunjukkan dalam pendahuluan. Dengan
										merujuk dari berbagai sumber pustaka, pandangan singkat dari para
										penulis/peneliti lain yang pernah melakukan pembahasan topik terkait
										dapatdikemukakan di sini untuk menerangkan kemutakhiran substansi
										pekerjaan.</p>
									<p align="justify">TUJUAN</p>
									<p align="justify">Tujuan substansi artikel ilmiah harus diungkapkan secara jelas dan mencerminkan judul artikel.</p>
									<p align="justify">METODE</p>
									<p align="justify">Judul dari bagian ini dapat diganti dengan Metode Penelitian, Metode
										Pelaksanaan atau Bahan dan Metode atau judul lain bergantung pada
										kegiatan dan metodologi yang telah dilakukan sehingga penulis diberi
										kebebasan untuk memberi judul lain seperti Pendekatan Teoritik atau
										Konsideran Percobaan. Secara umum, metode berisi tentang bagaimana
										observasi dilakukan termasuk waktu, lama, dan tempat dilakukannya
										observasi, bahan dan alat yang digunakan, metode untuk memperoleh
										data/informasi, serta cara pengolahan data dan analisis yang dilakukan.
										Metode harus dijelaskan secara lengkap agar peneliti lain dapat melakukan
										uji coba ulang. Acuan (referensi) pelaksanaan harus dimunculkan agar
										detail acuannya dapat ditelusuri.</p>
									<p align="justify">HASIL DAN PEMBAHASAN</p>
									<p align="justify">Bagian ini menjelaskan tentang apa saja yang diperoleh dari observasi.
										Data dapat diringkas dalam bentuk tabel dan gambar. Tidak ada spekulasi
										dan interpretasi dalam bagian ini, yang ada hanya fakta. Umumnya berisi
										uraian dan analisis berkaitan dengan temuan-temuan dari observasi yang
										telah dilakukan, terutama dalam konteks yang berhubungan dengan apa
										yang pernah dilakukan oleh orang lain. Interpretasi dan ketajaman analisis
										dari penulis terhadap hasil yang diperoleh dikemukakan di sini, termasuk
										pembahasan tentang pertanyaan-pertanyaan yang timbul dari hasil
										observasi serta dugaan ilmiah yang dapat bermanfaat untuk penelitian
										mendatang. Pemecahan masalah yang berhasil dilakukan, perbedaan dan
										persamaan dari hasil pengamatan terhadap informasi yang ditemukan
										dalam berbagai pustaka (penelitian terdahulu) perlu mendapatkan catatan
										di sini. Hasil dan Pembahasan handaknya menjadi satu kesatuan dan tidak
										dipisah menjadi subbab tersendiri</p>
									<p align="justify">KESIMPULAN</p>
