				<div align="justify">
					<h4>1. Pendahuluan</h4>
					<p align="justify">PKM-KC merupakan program konstruktif berbasis karsa dan nalar mahasiswa,
						meskipun belum memberikan nilai fungsional yang sempurna dan atau kemanfaatan
						langsung bagi pihak lain. Produk PKM-KC harus sudah berada pada skala siap pakai
						dan fungsional atau minimal skala prototip siap diuji coba. PKM-KC menekankan
						keaslian ide atau minimal modifikasi produk eksisting dan bukan karya tiruan. Contoh
						: Pratima yang merupakan karya nyata umat Hindu menjadi salah satu bagian penting
						Pura di Bali. Karena bersifat sakral, nilai budaya yang tinggi, kedudukannya yang
						tidak terpancang, tidak ada penjagaan khusus menyebabkan pratima rentan dicuri
						orang. Sampai saat ini belum ada upaya melindunginya dari pencuri barangbarang
						antik dan berharga. Membuatkan sangkar gelombang elektromagnetik tidak kasat
						mata di sekitar pratima, berbasis sensor suhu tubuh manusia, yang segera berdenging
						menulikan telinga berpotensi menjadi produk yang atraktif.</p>
					<div align="justify">
						<h4>2. Tujuan</h4>
						<p align="justify">Tujuan PKM-KC adalah menumbuh kembangkan kreativitas dan inovasi
							mahasiswa berbasis iptek dalam upaya menghasilkan suatu produk baik itu berwujud
							sistem, desain, model/barang, prototip, produk kesenian kontemporer, aplikasi,
							produk literasi atau jasa layanan yang bermanfaat bagi masyarakat luas atau instansi
							pemerintah atau dunia usaha</p>
						<div align="justify">
							<h4>3. Ruang Lingkup</h4>
							<p align="justify">Kegiatan dan produk PKM-KC meliputi semua bidang keilmuan dan disarankan
								agar sesuai atau relevan dengan kepakaran tim pengusul (mono atau multi disiplin
								ilmu). Posisi PKM-KC adalah pasca PKM-P. Sumber inspirasi dalam PKM-KC
								antara lain:</p>
							<p align="justify">a. Hasil riset yang baru sampai tahap desain teknis dan belum menghasilkan
								prototipe atau lainnya yang siap diujicoba</p>
							<p align="justify">b. Solusi atas persoalan, kebutuhan atau tantangan yang dihadapi masyarakat,
								pemerintah, ataupun dunia usaha. Jika tidak ditemukan hasil riset yang dapat
								dijadikan dasar kegiatan PKM-KC, maka riset aplikatif yang menghasilkan
								produk fungsional disetarakan denganPKM-KC</p>
							<p align="justify">c. Pengembangan atau penyempurnaan fungsi produk yang ada dengan tetap
								memperhatikan kaidah-kaidah keilmuan yang relevan dengan menunjukkan
								di mana letak perbedaannya</p>
							<p align="justify">Tahap akhir PKM-KC adalah Fase Implementasi di mana produk dapat
								difungsikan dan dinilai level kemanfaatannya. Dalam kasus tertentu, jika produk
								PKM-KC belum fungsional, paling tidak fase Konstruksi sudah tercapai dan
								dilakukan uji coba. Produk PKM-KC dapat menjadi titik pijak pengusulan dan
								pelaksanaan PKM-K, PKM-M atau PKM-T pada tahun-tahun berikutnya.
								Beberapa contoh produk PKM-KC dapat disebutkan antara lain: kalkulator amal
								dan dosa, scanner penentu usia korban kecelakaan, aplikasi gojek, rumah hunian
								nyaman di lingkungan kumuh, detektor gelembung udara dalam infus, tarian
								penyambutan mahasiswa baru, ukiran gaya kontemporer, musik, atau lukisan
								unik/kontemporer terkait aspek kesehatan, pendidikan, disabilitas dan lain-lain.</p>
