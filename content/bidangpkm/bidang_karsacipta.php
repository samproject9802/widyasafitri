											<h4>7. Sistematika Penulisan Laporan</p>
											</h4>
											<p align="justify">Judul PKM tidak boleh menggunakan akronim atau singkatan yang tidak baku dan
												hanya diperbolehkan maksimal 20 kata. Mahasiswa menyusun laporan PKM yaitu
												isian kelengkapan (sampul dan pengesahan), halaman utama (daftar isi, halaman inti
												dan lampiran), halaman inti (pendahuluan sampai dengan daftar pustaka) maksimum
												50 halaman. Format penulisan mengikuti sistematika sebagai berikut:</p>
											<p align="justify">SAMPUL</p>
											<p align="justify">LEMBAR PENGESAHAN</p>
											<p align="justify">DAFTAR ISI</p>
											<p align="justify">RINGKASAN</p>
											<p align="justify">BAB 1. PENDAHULUAN</p>
											<p align="justify">Uraikan proses identifikasi persoalan yang akan dicari solusi atau
												pengembangannya termasuk sumber inspirasinya. Jika titik pijaknya
												adalah hasil riset orang lain, maka nyatakan nama pelaksana dan institusi
												tim riset serta hasilnya yang akan dikonstruksikan dalam PKM-KC.
												Ungkapkan pula fase final yang akan dicapai dalam PKM-KC. Jika akan
												melakukan pengembangan atau penyempurnaan atas produk eksisting di
												masyarakat atau sudah digunakan di kalangan terbatas, maka nyatakan
												nama produsen/ pembuat dan institusinya. Jangan lupa ungkapkan target
												yang akan dicapai dan aspek pengembangan/ penyempurnaan yang akan
												dilakukan disertai justifikasi ilmiah dan/atau aspek ekonominya.
												Jika produk PKM-KC harus dibuat mulai dari titik NOL, artinya belum
												ada produk riset sebelumnya yang dapat dijadikan pijakan, juga tidak ada
												produk yang ditemukan/digunakan di masyarakat, maka ungkapkan target
												fungsionalnya disertai justifikasi ilmiah yang akhirnya dimuarakan pada
												desain sebelum dikonstruksikan menjadi produk/jasa final yang
												fungsional. Pada Bab 1 ini pula, nyatakan Luaran PKM-KC yang
												ditargetkan dan manfaatnya.
											<p align="justify">BAB 2. TINJAUAN PUSTAKA</p>
											<p align="justify">Pada PKM-KC ada kemungkinan pustaka acuan seperti yang lazim disitasi
												untuk PKM-P tidak ditemukan. Selain skripsi, tesis, disertasi, buku
												referensi, artikel jurnal ilmiah ataupun prosiding, tinjauan pustaka dalam
												PKM-KC diijinkan untuk mengacu pada informasi yang diperoleh melalui
												internet, brosur, media cetak dan sumber-sumber informasi lainnya. Yang
												penting dalam bab ini adalah terungkapnya informasi ilmiah yang relevan
												dengan spesifikasi awal dan/atau akhir produk serta menjadi solusi yang
												bermanfaat.
											<p align="justify">BAB 3. METODE PENELITIAN</p>
											<p align="justify">Pada bagian ini diuraikan tahap pelaksanaan program dan fase akhir yang
												akan dicapai secara rinci Dimulai dari koleksi data yang diperlukan untuk
												desain atau perancangan awal, menyusun desain teknis, membuat
												produk/jasa layanan, menguji keandalan karya, evaluasi level penerimaan
												masyarakat (jika dimungkinkan) dan lain-lain yang relevan.
