			<div align="justify">
				<h4>1. Pendahuluan</h4>
				<p align="justify">Program Kreativitas Mahasiswa Penerapan Teknologi (PKM-T) merupakan
					program bantuan ilmu pengetahuan dan teknologi (iptek) untuk mitra usaha berskala
					mikro atau kecil (toko, industri rumahan, pedagang kaki lima atau koperasi) dan
					menengah bahkan berskala besar sesuai persoalan atau kebutuhan prioritas mitra
					program. Bantuan iptek dapat berupa peningkatan mutu produk, perbaikan proses
					produksi, peningkatan kapasitas produksi, pengolahan limbah, sistem jaminan mutu,
					kemasan dan lain-lain, atau bantuan manajemen seperti konflik sosial SDM,
					pemasaran, pembukuan, status usaha, hak cipta, dan lain-lain. Mitra usaha PKM-T
					yang dimaksud adalah kelompok masyarakat yang berorientasi pada profit termasuk
					kelompok tani, kelompok nelayan, sanggar tari, klinik bersalin dan lain-lain. Sebelum
					menyusun proposal PKM-T, mahasiswa harus sudah bertukar pikiran dengan mitra
					dalam rangka mengidentifikasi permasalahan atau kebutuhan mitra, karena produk
					PKM-T merupakan solusi atas persoalan prioritas mitra. Jadi persoalan PKM-T bukan
					merupakan inisiatif mahasiswa, tetapi berasal dari permintaan mitra. Dengan
					demikian, di dalam proposal PKM-T harus dilampirkan Surat Pernyataan Kesediaan
					Bekerjasama dari Mitra. Kekeliruan dalam memilih mitra dapat mengakibatkan
					kesalahan dalam pemilihan slot program menjadi PKM-M.</p>
				<div align="justify">
					<h4>2. Tujuan</h4>
					<p align="justify">Tujuan PKM-T adalah memotivasi mahasiswa untuk aktif berinteraksi
						membangun jejaring profesional dengan dunia usaha, mengidentifikasi persoalan atau
						kebutuhan mitra usaha dan menemukan solusinya.</p>
					<div align="justify">
						<h4>3. Ruang Lingkup</h4>
						<p align="justify">Ruang lingkup dari kreativitas PKM-T adalah memberikan bantuan iptek kepada
							mitra usaha sebagai bentuk solusi atas permasalahan prioritas mitra. Pada hakekatnya
							PKM-T terbuka bagi semua bidang ilmu karena teknologi secara luas dapat dimaknai
							sebagai cara untuk memadukan sumber-sumber produksi, guna menghasilkan
							produk-produk yang dikehendaki baik barang maupun jasa, menyelesaikan masalah,
							memenuhi kebutuhan, atau memuaskan keinginan. Ilmu dan teknologi yang
							diperbantukan tidak terbatas pada bidang teknik semata, tetapi juga keilmuan bidang
							sosial humaniora. Dengan demikian, kegiatan PKM-T meliputi aspek sumber daya
							manusia, bahan baku, proses dan peralatan produksi, desain kemasan, manajemen,
							promosi dan pemasaran produk, status usaha, dan perlindungan hak cipta.</p>
