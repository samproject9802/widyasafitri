										<h4>7. Sistematika Penulisan Laporan</h4>
										<p align="justify">Judul PKM tidak boleh menggunakan akronim atau singkatan yang tidak baku dan
											hanya diperbolehkan maksimal 20 kata. Mahasiswa menyusun laporan PKM yaitu
											isian kelengkapan (sampul dan pengesahan), halaman utama (daftar isi, halaman inti
											dan lampiran), halaman inti (pendahuluan sampai dengan daftar pustaka) maksimum
											50 halaman. Format penulisan mengikuti sistematika sebagai berikut:</p>
										<p align="justify">SAMPUL</p>
										<p align="justify">LEMBAR PENGESAHAN</p>
										<p align="justify">DAFTAR ISI</p>
										<p align="justify">RINGKASAN</p>
										<p align="justify">BAB 1. PENDAHULUAN</p>
										<p align="justify">Pada bab ini, ungkapkan proses identifikasi masalah bersama mitra yang akan diselesaikan. Upayakan untuk memperoleh persoalan prioritas mitra yang diamati mulai dari aspek hulu sampai dengan hilirnya. PKM-T disarankan hanya fokus pada satu persoalan prioritas, baik dari aspek teknologi maupun sosial humaniora. Pada kedua aspek itupun cukup satu persoalan prioritas mitra yang ditangani, apakah bagian hulu, proses,atau hilirnya. Jelaskan pada aspek mana bantuan ilmu dan teknologi yang ditawarkan diyakini akan mampu meningkatkan nilai tambah bagi mitra.
											Misalnya peningkatan mutu produk, perbaikan proses produksi, pengolahan limbah, sistem jaminan mutu dan lain-lain atau aspek-aspek manajemen yang mencakup perbaikan kualitas pola interaksi SDM, pemasaran, pembukuan atau status usaha.Ungkapkan pula profil usaha dan kinerja mitra yang akan dilibatkan dalam kegiatan PKM-T secara kuantitatif. Pada bab ini pula disajikan jenis Luaran yang akan dihasilkan disertai penjelasan manfaatnya bagi mitra.
										<p align="justify">BAB 2. TINJAUAN PUSTAKA</p>
										<p align="justify">Uraikan secara ilmiah semua hal yang relevan dengan persoalan atau
											kebutuhan prioritas mitra yang akan diselesaikan, termasuk solusi yang
											ditawarkan. Jika solusinya sudah pernah dipublikasikan pihak lain, agar
											diungkapkan juga. Yang diutamakandalam PKM-T adalah ketepatan
											solusi iptek yang akan diberikan kepada mitra. Solusi yang ditawarkan
											dapat berupa karya orisinil dan dapat juga berupa karya tiruan dari pihak
											lain.
										<p align="justify">BAB 3. METODE PENELITIAN</p>
